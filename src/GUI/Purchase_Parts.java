	private void loadSpareParts() {

		try {

			jTablepro.removeAll();
			jTablepro.repaint();
			jTablepro.revalidate();
			DefaultTableModel dtm = (DefaultTableModel) jTablepro.getModel();
			dtm.getDataVector().removeAllElements();
			ResultSet sr = DB_connection.search("select * from Spare_part WHERE Stock_quantity <= 10");

			while (sr.next()) {

				Vector v = new Vector();
				v.add(sr.getString("idSpare_part"));
				v.add(sr.getString("Name"));
				v.add(sr.getString("Parts_brand"));
				v.add(sr.getString("Mountable_year"));
				v.add(sr.getString("Mountable_moddle"));
				v.add(sr.getString("Stock_quantity"));
				v.add(sr.getString("Price"));

				dtm.addRow(v);
			}
		} catch (Exception e) {
			e.printStackTrace();
		}

	}
	

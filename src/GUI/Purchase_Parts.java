    private void txtToKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtToKeyReleased
        try {
			
				if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
                jListServiceId.grabFocus();
                jListServiceId.setSelectedIndex(0);
            } else if (txtTo.getText().equals("")) {
                jScrollPane2.setVisible(false);
            } else {
                ResultSet rs = DB_connection.search("select * from Part_supplier where Email like '%" + txtTo.getText() + "%'");
                Vector v = new Vector();
                while (rs.next()) {
                    v.add(rs.getString("Email"));
                }
                jListServiceId.setListData(v);
                if (v.equals("")) {
                    jScrollPane2.setVisible(false);
                }
                jScrollPane2.setVisible(true);
                jPanel8.updateUI();
            }
		} catch (Exception e) {
			e.printStackTrace();
		}
    }//GEN-LAST:event_txtToKeyReleased

    
